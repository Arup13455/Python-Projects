# program to calculate area of a square,a circle,a rectangle
side = input("enter side:")
square_area = int(side) * int(side)
print("square_area is:", int(square_area))
radius = input("enter radius:")
circle_area = 3.14 * int(radius) * int(radius)
print("circle_area is:", float(circle_area))
length = input("enter length:")
breadth = input("enter breadth:")
rectangle_area = int(length) * int(breadth)
print("rectangle_area is:", float(rectangle_area))

